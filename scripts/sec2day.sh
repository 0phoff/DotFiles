# By 0phoff
# MIT license
#
#--------------------------------------------------------------------------------------------------------
#
# Usage: sec2day seconds
#
#--------------------------------------------------------------------------------------------------------

if [ $# -eq 0 ]; then
    echo "Please enter seconds!"
    exit
fi

echo $(($1/86400))d $(($(($1 - $1/86400*86400))/3600))h:$(($(($1 - $1/86400*86400))%3600/60))m:$(($(($1 - $1/86400*86400))%60))s
